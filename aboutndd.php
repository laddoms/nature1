			<h3>What is Nature Deficit Disorder?</h3>
			<p>
			   In his book, ‘Last Child in the Woods, Richard Louv describes what he calls Nature Deficit Disorder (NDD). 
			   Nature Deficit Disorder is a non-medical condition that describes the effects of alienation from our natural 
			   environment, especially in young children. 
			</p>
			<p>
			   The 2008 Recreation Participation Report documents an eleven percent decline in outdoor activities amongst children
			   ages six to 17. The survey collected data from 60,000 Americans on 114 different outdoor activities. 
			</p>
			<div id="hawkimg">
				<img src="images/hawk.jpg" alt="image of a hawk">
			</div>
			<p>
				Scientific evidence suggests that nature deficit disorder can cause attention difficulties, diminished use of senses,
				obesity, higher rates of physical and emotional illness.  Nature Deficit Disorder can cause lack of concern or 
				protection for our natural world. 
			</p>
			<p>
				Lack of unstructured nature exploration and play can also cause ‘Nature Knowledge Deficit’. Nature Knowledge Deficit
			    is the general lack of understanding of how our natural world functions. Less time spent outdoors creates less
			    knowledge of how things grow, what wild animals are like, and how seasons work. 
			</p>
